    private void MarcarProgresoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_MarcarProgresoActionPerformed
                  
        
        
        TableModel model1 = jTable1.getModel();
        int indexs[] = jTable1.getSelectedRows();
        
        Object[] row = new Object[2];
        
        JFrame2 frm2 = new JFrame2();
        DefaultTableModel model2 = (DefaultTableModel)frm2.jTable1.getModel();
        
        for(int i = 0; i < indexs.length; i++)
        {
            row[0] = model1.getValueAt(indexs[i], 0);
            row[1] = model1.getValueAt(indexs[i], 1);
            
            model2.addRow(row);
        }
        
        frm2.setVisible(true);
               
        

    }//GEN-LAST:event_MarcarProgresoActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
                
        
        int response = JOptionPane.showConfirmDialog(this, "¿Quieres eliminar la tarea?", "Confirmación", JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);
        
        if(response==JOptionPane.YES_OPTION){
            JOptionPane.showMessageDialog(null, "Tarea eliminada con éxito", "Información", JOptionPane.PLAIN_MESSAGE);
          
            
        }else if(response==JOptionPane.NO_OPTION){
            JOptionPane.showMessageDialog(null, "Ha seleccionado no eliminar la tarea", "Información", JOptionPane.PLAIN_MESSAGE);
        }

        DefaultTableModel df1 = (DefaultTableModel) jTable1.getModel();
        int rs[] = jTable1.getSelectedRows();
        for (int i = rs.length-1; i >=0 ; i--) {

        int k = rs[i];

        df1.removeRow(k);

    }
    }//GEN-LAST:event_jButton1ActionPerformed

