    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed

    DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        
// get selected row index
        int selectedRowIndex = jTable1.getSelectedRow();
        
// get selected row data 
        String nombre = model.getValueAt(selectedRowIndex, 0).toString();
        String descripcion = model.getValueAt(selectedRowIndex, 1).toString();


 // get the entered data         
        String updN = JOptionPane.showInputDialog(null,"Introdusca el nombre",nombre);
        String updDesc = JOptionPane.showInputDialog(null,"Actualice la descripción",descripcion);

// set the new data into jtable row
        model.setValueAt(updN, selectedRowIndex, 0);
        model.setValueAt(updDesc, selectedRowIndex, 1);

    
    }//GEN-LAST:event_jButton5ActionPerformed

